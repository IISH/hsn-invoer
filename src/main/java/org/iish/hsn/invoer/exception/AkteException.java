package org.iish.hsn.invoer.exception;

public class AkteException extends Exception {
    public AkteException() {
        super();
    }

    public AkteException(String message) {
        super(message);
    }

    public AkteException(String message, Throwable cause) {
        super(message, cause);
    }

    public AkteException(Throwable cause) {
        super(cause);
    }
}
